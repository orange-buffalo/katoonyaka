package co.katoonyaka.web.admin.domain;

public class PhotoData {
    private String id;
    private int height;
    private int width;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public PhotoData() {

    }
}
